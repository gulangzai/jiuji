package com.jiuji.cn.model;

public class TPicture {
	
	private int fId;
	private String fProductId;
	private String fSmall;
	private String fMidPic;
	private String fBigPic;
	public int getfId() {
		return fId;
	}
	public void setfId(int fId) {
		this.fId = fId;
	}
	public String getfProductId() {
		return fProductId;
	}
	public void setfProductId(String fProductId) {
		this.fProductId = fProductId;
	}
	public String getfSmall() {
		return fSmall;
	}
	public void setfSmall(String fSmall) {
		this.fSmall = fSmall;
	}
	public String getfMidPic() {
		return fMidPic;
	}
	public void setfMidPic(String fMidPic) {
		this.fMidPic = fMidPic;
	}
	public String getfBigPic() {
		return fBigPic;
	}
	public void setfBigPic(String fBigPic) {
		this.fBigPic = fBigPic;
	} 
}
